package day01;

public class Actor {
    private String name;
    private int birthyear;

    public Actor(String name, int birthyear) {
        this.name = name;
        this.birthyear = birthyear;
    }

    public String getName() {
        return name;
    }

    public int getBirthyear() {
        return birthyear;
    }
}


